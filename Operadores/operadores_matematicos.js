idade = 25 + 5;
console.log("Somando: " + idade);

//Subtração
idade = 25 - 5;
console.log("Subtração: " + idade);

//Multiplicação
idade = 25 * 5;
console.log("Multiplicação: " + idade);

//Divisão
idade = 25 / 5;
console.log("Divisão: " + idade);

//Módulo. Resto da divisão
idade = 25 % 5;
console.log("Módulo: " + idade);

